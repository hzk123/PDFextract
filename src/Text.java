public class Text {
	float X, Y, fs, xscale, height, space, width;
	String unicode;

	Text(float X, float Y, float fs, float xscale, float height, float space, float width, String u) {
		this.X = X;
		this.Y = Y;
		this.fs = fs;
		this.xscale = xscale;
		this.height = height;
		this.space = space;
		this.width = width;
		this.unicode = u;
	}
};